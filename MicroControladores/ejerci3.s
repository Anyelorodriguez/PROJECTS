GPIO_PORTF_DATA_R      EQU     0x400253FC
GPIO_PORTF_DIR_R       EQU     0x40025400
GPIO_PORTF_AFSEL_R     EQU     0x40025420
GPIO_PORTF_PUR_R       EQU     0x40025510
GPIO_PORTF_DEN_R       EQU     0x4002551C
GPIO_PORTF_LOCK_R      EQU     0x40025520
GPIO_PORTF_CR_R        EQU     0x40025524
GPIO_PORTF_AMSEL_R     EQU     0x40025528
GPIO_PORTF_PCTL_R      EQU     0x4002552C
SYSCTL_RCGC2_R     	 EQU       0x400FE108
GPIO_LOCK_KEY          EQU     0x4C4F434B

		AREA    |.text|, CODE, READONLY, ALIGN=2
		THUMB
		EXPORT  __main
				
__main
		LDR		R0,=SYSCTL_RCGC2_R			;*RO APUNTE A UNA POSICION DE MEMORIA
		LDR		R1, [R0]					;R1 = A R0
		MOV		R2,#0X20					;R2 = 0x02
		ORR		R1,R1,R2					;r1 = r1 or r2
		STR		R1, [R0]					; GUARDA EN LA POSICION DE MEMORIA R0 EL DATO DE R1 *R0 = R1
		NOP									;NO HACE NADA EN UN CICLO DE RELOJ (DELAY UN CICLO)
		NOP									;NO HACE NADA EN UN CICLO DE RELOJ (DELAY UN CICLO)
		NOP									;NO HACE NADA EN UN CICLO DE RELOJ (DELAY UN CICLO)
		
	;GPIO_PORTF_LOCK_R=GPIO_LOCK_KEY CONFIGURAR PUERTO R 
	
		LDR		R0, =GPIO_PORTF_LOCK_R
		LDR		R1, =GPIO_LOCK_KEY
		STR		R1,[R0]
		
	;GPIO_PORTF_CR_R= 0xFF CONFIGURAR EL PUERTO CR
		LDR		R0, =GPIO_PORTF_CR_R
		MOV		R1, = #0xFF
		STR		R1,[R0]
	;GPIO_PORTF_AMSEL_R= 0x00
	;deshabilitar el modo analogo en puerto f PORTF
		LDR		R0, =GPIO_PORTF_AMSEL_R
		MOV		R1, = #0x00
		STR		R1,[R0]
		
	;GPIO_PORTF_PCTL_R= 0x00000000
	;NO ACTIVA LOS PERIFERICOS EN EL PORTF
		LDR		R0, =GPIO_PORTF_PCTL_R
		MOV		R1, = #0x00000000
		STR		R1,[R0]	
	
	;GPIO_PORTF_DIR_R= 0x00000000
	;CONFIGURA ENTRADAS Y SALIDAS EN I/O EN PORT F
	;F4  -  F3  -  F2  -  F1  -  F0
	;SW1 - LEDG - LEDB - LEDR - SW2
	;0   -  1   -  1   -   1  -  0  = 0x0E   los unos son entradas los 0 son salidas
	;0 = ENTRADA
	;1 = SALIDA
	
		LDR		R0, =GPIO_PORTF__R
		MOV		R1, = #0x00000000
		STR		R1,[R0]	
	
	;GPIO_PORTF_AFSEL_R= 0x00
	;
		LDR		R0, =GPIO_PORTF_AFSEL_R
		MOV		R1, = #0x00
		STR		R1,[R0]	
	;GPIO_PORTF_PUR_R= 0x11
	;CONFIGURA PULL UP PARA PF0 Y PF4
		LDR		R0, =GPIO_PORTF_PUR_R
		MOV		R1, = #0x11
		STR		R1,[R0]	
	;GPIO_PORTF_DEN_R= 0x1F
	;HABILITA MODO DIGITAL PARA PF0 Y PF4
		LDR		R0, =GPIO_PORTF_DEN_R
		MOV		R1, = #0x1F
		STR		R1,[R0]	
		
		LDR		R12, =GPIO_PORTF_DATA_R
		
INICIO	MOV		R0, #0X02
		STR		R0, [R12]
		
		MOV		R0, #0X00
		STR		R0, [R12]
		
		B
		
	
	
	
	;GPIO_PORTF__R= 0x00000000
	;
		
	ALIGN
	END
		
		
		